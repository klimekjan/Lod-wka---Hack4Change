def generuj() -> None:
    wiersze: list[tuple[str, str]] = []
    for kategoria, nazwy in DANE.items():
        for nazwa in nazwy:
            wiersze.append((nazwa, kategoria))

    with open(OUT, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["nazwa", "kategoria"])
        w.writerows(wiersze)

    from collections import Counter
    cnt = Counter(k for _, k in wiersze)
    print(f"Wygenerowano {len(wiersze)} próbek w {len(cnt)} kategoriach:")
    for kat, n in sorted(cnt.items()):
        print(f"  {kat}: {n}")
    print(f"\nZapisano do: {OUT}")
    print("Teraz uruchom: python -m app.services.ml.train_classifier")


if __name__ == "__main__":
    generuj()
